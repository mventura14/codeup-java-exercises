public class StringExercise {
    public static void main(String[] args) {
        String str1 = "We don't need no education\n" +
                "We don't need no thought control";

        System.out.println(str1);

        str1 = "Check \"this\" out!, \"s inside of \"s!\"";

        System.out.println(str1);

        str1 = "In windows, the main drive is usually C:\\ ";
        System.out.println(str1);

        str1 = "I can do backslashes \\, double backslashes \\\\,\n" +
                "and the amazing triple backslash \\\\\\!";

        System.out.println(str1);


    }
}
